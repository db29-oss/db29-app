  <pre><b>Câu hỏi: tôi muốn tải về toàn bộ dữ liệu của tôi để tự lưu trữ, tự host được không?</b></pre>
  <pre>Được bạn, một số phần mềm tự động có tính năng trích xuất dữ liệu được xây dựng bên trong phần mềm, một số phần mềm không hỗ trợ, mình sẽ viết ra tính năng trích xuất dữ liệu bao gồm cơ sở dữ liệu và tất cả các file đính kèm.</pre>

  <br>

  <pre><b>Câu hỏi: tôi sử dụng domain riêng của tôi được không?</b></pre>
  <pre>Được bạn, đọc hướng dẫn dùng CNAME để bạn xài ứng dụng mình host nhưng trên domain của bạn.</pre>

  <br>

  <pre><b>Câu hỏi: tôi có thể sử dụng server của tôi hay không?</b></pre>
  <pre>Được nha bạn, bạn có thể thêm kết nối ssh vào mục server của tôi, khi tạo mới phần mềm sẽ ưu tiên sử dụng server của bạn.</pre>

  <br>

  <pre><b>Câu hỏi: mô hình kinh doanh của bạn là gì?</b></pre>
  <pre>Mặc dù mã nguồn là mở, nhưng để phần mềm có thể xuất hiện được trên internet cần nhiều ở khâu vận hành hậu cần, vd: đảm bảo phần mềm luôn sẵn sàng khi bạn cần nó, nâng cấp mã nguồn lên phiên bản mới, cập nhật các bản vá lỗi, nâng cấp hệ điều hành lên phiên bản mới và hơn nữa khi phần mềm chạy cần tiêu tốn tài nguyên. Nếu bạn không có thời gian, mình có thể vận hành giúp bạn với một mức phí nhỏ.</pre>

  <br>

  <pre><b>Câu hỏi: liệu có bị lộ hay mất mát dữ liệu của tôi không?</b></pre>
  <pre>Bạn yên tâm nhé, dữ liệu của bạn sẽ không bị mất hay lộ đi đâu cả, mình sử dụng raid ổ cứng để lưu trữ dữ liệu, kể cả hư hỏng phần cứng vẫn không mất dữ liệu, hơn nữa mình cũng backup dữ liệu thường xuyên, xui nhất sẽ là mất dữ liệu trong vòng 1h gần nhất thôi.</pre>

  <br>

  <pre><b>Câu hỏi: vì sao ứng dụng của tôi lại bị tắt hằng ngày?</b></pre>
  <pre>Khi mới đăng ký tài khoản, bạn sẽ được tặng một số dư tài khoản dùng để trải nghiệm thử ứng dụng. Sau khi hết số dư, các ứng dụng sẽ bị tắt, bạn có thể vào trang web để khởi động ứng dụng lại, nhưng số dư tài khoản sẽ bị âm và ứng dụng vẫn sẽ bị tắt sau 24h.</pre>

  <br>

  <pre><b>Câu hỏi: ứng dụng và tài khoản của tôi có thể bị xóa không?</b></pre>
  <pre>Ứng dụng sẽ bị xóa nếu như không được kích hoạt khởi động lại trong 30 ngày. Tài khoản sẽ bị xoá nếu không còn ứng dụng nào và cũng không đăng nhập trong 30 ngày.</pre>

  <br>

  <pre><b>Câu hỏi: giá các gói cước có thể bị thay đổi không?</b></pre>
  <pre>Có thể bị thay đổi, giá trị tiền lạm phát theo hằng năm, mình sẽ cần phải điều chỉnh lại cho hợp lý. Cơ chế giá là có một gói cơ bản, các gói khác sẽ là gói cơ bản nhân với một hệ số. Ví dụ: gói cơ bản là 50k 1 tháng (tùy ứng dụng), gói cao cấp hơn gấp rưỡi là 75k, khi mình thay đổi giá gói cơ bản 50k trở thành 55k, gói cao cấp sẽ trở thành 82.5k.</pre>

